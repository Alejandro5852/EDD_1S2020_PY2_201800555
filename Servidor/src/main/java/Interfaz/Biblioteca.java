    private void actualizar() {
        SimpleMenteEnlazada bibliotecaDeUsuario = servidor.bibliotecaUsuario(usuario.getCarnet());
        for (int z = jTable1.getRowCount() - 1; z >= 0; z--) {
            modeloMiBiblioteca.removeRow(z);
        }
        for (int i = 0; i < bibliotecaDeUsuario.Tamaño(); i++) {
            Libro lib = (Libro) bibliotecaDeUsuario.at(i);
            modeloMiBiblioteca.addRow(new Object[]{lib.getISBN(), lib.getTitulo(), lib.getAutor(), lib.getCategoria()});
        }
        jLabel14.setText("(" + bibliotecaDeUsuario.Tamaño() + ")");
    }

    private int indice(int ISBN) {
        SimpleMenteEnlazada bibliotecaDeUsuario = servidor.bibliotecaUsuario(usuario.getCarnet());
        int indice = -1;
        for (int i = 0; i < bibliotecaDeUsuario.Tamaño(); i++) {
            Libro temp = (Libro) bibliotecaDeUsuario.at(i);
            if (temp.getISBN() == ISBN) {
                indice = i;
                break;
            }
        }
        return indice;
    }

    private int indice(String Titulo) {
        SimpleMenteEnlazada bibliotecaDeUsuario = servidor.bibliotecaUsuario(usuario.getCarnet());
        int indice = -1;
        for (int i = 0; i < bibliotecaDeUsuario.Tamaño(); i++) {
            Libro temp = (Libro) bibliotecaDeUsuario.at(i);
            if (temp.getTitulo().compareTo(Titulo) == 0) {
                indice = i;
                break;
            }
        }
        return indice;
    }

    private Libro getLibro(int indice) {
        return (Libro) servidor.bibliotecaUsuario(usuario.getCarnet()).at(indice);
    }

